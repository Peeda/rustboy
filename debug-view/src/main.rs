fn spread(val:u8) -> u16 {
    let mut out:u16 = 0;
    for i in 0..8 {
        if val & (1 << i) > 0 {
            out |= 1u16 << (2*i);
        }
    }
    out
}
fn write_tile(tile: &[u8], buffer: &mut [u32]) {
    assert_eq!(tile.len(), 16);
    assert_eq!(buffer.len(), 64);
    let mut merged:[u16; 8] = [0; 8];
    for i in 0..8 {
        // 00111100
        // 01111110
        // -------- spread
        // 0000010101010000
        // 0010101010101000
        // -------- merge
        // 0010111111111000
        merged[i] = spread(tile[2*i]) | ((spread(tile[2*i + 1]) << 1));
        //println!("{:016b}", merged[i]);
    }
    for i in &mut *buffer {
        *i = 0x00000000;
    }
    for i in 0..buffer.len() {
        let row = i/8;
        let col = 7 - i%8;
        buffer[i] = match (merged[row] & (0x03 << col * 2)) >> col * 2 {
            0b00 => 0xff000000,
            0b01 => 0xff555555,
            0b10 => 0xffaaaaaa,
            0b11 => 0xffffffff,
            _ => unreachable!()
        }
    }
}
fn main() {
    let (mut rl, thread) = raylib::init()
        .size(1280, 760)
        .title("Hello, World")
        .build();
     
    let mut textures = Vec::new();
    let mut i = 0;
    while i < DATA.len() {
        let mut buffer:[u32; 64] = [0; 64];
        write_tile(&DATA[i..i+16], &mut buffer);
        let img = Image::gen_image_color(8, 8, Color::PURPLE);
        let mut tex = rl.load_texture_from_image(&thread, &img).unwrap();
        unsafe {
            tex.update_texture(&std::mem::transmute::<[u32;64], [u8;256]>(buffer));
        }
        textures.push(tex);
        i += 16;
    }

    while !rl.window_should_close() {
        let mut d = rl.begin_drawing(&thread);
         
        d.clear_background(Color::WHITE);

        for i in 0..16 {
            for k in 0..24 {
                d.draw_texture_ex(&textures[i*16+k], math::Vector2::new((k*16) as f32, (i*16) as f32), 0., 2., Color::WHITE);
            }
        }
        d.draw_texture_ex(&textures[0], math::Vector2::new(12., 12.), 0., 2., Color::WHITE);
    }
}
